//#include <REG51.h> //通用89C51头文件
#include <STC12C5620AD.H> // STC12C5616AD 凑合用
//#include <STC12C5A60S2.H> // STC12C5A60S2 系列单片机头文件


sbit P17 = P1^7;



/*********************************************************************************************
函数名：毫秒级CPU延时函数
调  用：DELAY_MS (?);
参  数：1~65535（参数不可为0）
返回值：无
结  果：占用CPU方式延时与参数数值相同的毫秒时间
备  注：应用于1T单片机时i<600，应用于12T单片机时i<125
/*********************************************************************************************/
void DELAY_MS (unsigned int a){
	unsigned int i;
	while( --a != 0){
		for(i = 0; i < 600; i++);
	}
}
/*********************************************************************************************/



/*********************************************************************************************
函数名：主函数
调  用：无
参  数：无
返回值：无
结  果：程序开始处，无限循环
备  注：
/**********************************************************************************************/
void main (void){
	P17 = 1; // 蜂鸣器正极接Vcc，负极接P17

	while(1){
		unsigned char i;
		for(i=0; i<100; i++){
			DELAY_MS(2);
			P17 = ~P17;
		}
		
		for(i=0; i<100; i++){
			DELAY_MS(3);
			P17 = ~P17;
		}
		
		for(i=0; i<100; i++){
			DELAY_MS(4);
			P17 = ~P17;
		}

	}
}


















