/*********************************************************************************************
函数名：UART串口发送字符串函数
调  用：UART_TC (?);
参  数：需要UART串口发送的数据（8位/1字节）
返回值：无 
结  果：向串口发送一个字符串,长度不限。
备  注：例：UART_TC("d9887321$"); 此函数需要#include <string.h>头文件支持。
/**********************************************************************************************/
void UART_TC (unsigned char *str){
	while(*str != '\0'){
		UART_T(*str);
		*str++;
	}
	*str = 0;
}
